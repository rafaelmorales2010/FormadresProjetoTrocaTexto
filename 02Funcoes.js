//Função normal
function soma1 (){
    let valor1 = 10;
    let valor2 = 10;

    return valor1 + valor2;

}

console.log(soma1());



//Função normal return
function media (){
    let valor1 = 10;
    let valor2 = 10;
    let valor3 = 8;

    return (valor1 + valor2 + valor3) / 3;

}

console.log(media());




//Função com parâmetros
function somaParametros(v1, v2){
    return v1 + v2;

}

console.log(somaParametros(10, 40));